public class Carro {

    String cor;
    String modelo;
    int quantoCusta;

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public int getQuantoCusta() {
        return quantoCusta;
    }

    public void setQuantoCusta(int quantoCusta) {
        this.quantoCusta = quantoCusta;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }


}
